// ── 2. ZIP-ENTRY PATH TRAVERSAL ─────────────────────────────────────────────

/// A docx that also contains an entry literally named
/// `../../../../tmp/attune_evil_marker` (and a `..\..\..` Windows variant). The
/// parser extracts `word/document.xml` to MEMORY and never iterates/writes other
/// entries to disk, so there must be no filesystem escape. We assert:
///   (a) parse succeeds + returns the benign body (the traversal entry is inert),
///   (b) NO file appears at the traversal target on disk.
#[test]
fn docx_zip_entry_path_traversal_no_fs_escape() {
    // Pre-clean the would-be escape targets so a stale file can't false-pass.
    let escape_unix = std::path::Path::new("/tmp/attune_evil_marker");
    let _ = std::fs::remove_file(escape_unix);

    let res = parse_with_timeout(fixture("traversal.docx"), "traversal.docx", DEADLINE);
    let (_title, content) = res.expect("traversal docx must parse the benign body, not error");
    assert!(
        content.contains("ATTUNE_TRAVERSAL_BODY"),
        "benign body must be extracted from word/document.xml: {content:?}"
    );
    // The malicious entry name must NEVER materialize a file outside the archive.
    assert!(
        !escape_unix.exists(),
        "SECURITY: zip-entry path traversal wrote a file to {} — parser must extract to \
         memory only, never honor entry names as filesystem paths",
        escape_unix.display()
    );
    // The PWNED payload must not leak into indexed content either.
    assert!(
        !content.contains("PWNED_TRAVERSAL"),
        "traversal payload entry leaked into extracted content: {content:?}"
    );
}

// ── 3. BILLION-LAUGHS / XXE in word/document.xml ────────────────────────────

/// docx whose word/document.xml carries a billion-laughs DTD entity bomb
/// (`&lol9;` would expand to ~3 GB if expanded) plus an external-entity
/// `<!ENTITY xxe SYSTEM "file:///etc/passwd">`. attune's `strip_xml_tags` is a
/// character scanner that does NOT process DTDs / expand entities / fetch
/// external resources, so this must be inert: bounded time, no entity blowup,
/// and NO /etc/passwd content in the output.
#[test]
fn docx_billion_laughs_and_xxe_inert_bounded() {
    let res = parse_with_timeout(fixture("laughs.docx"), "laughs.docx", DEADLINE);
    let (_title, content) = res.expect("billion-laughs docx must parse bounded, not error/hang");

    // Bounded output — no 3 GB entity explosion. The body is tiny; a few KB max.
    assert!(
        content.len() < 1_000_000,
        "SECURITY: output is {} bytes — entity expansion blew up (billion-laughs not inert)",
        content.len()
    );
    // The benign literal text survives (entities left as-is / dropped, not expanded).
    assert!(
        content.contains("safe text") && content.contains("ATTUNE_LAUGHS_BODY"),
        "benign body text must survive: {content:?}"
    );
    // XXE: no external-entity fetch — /etc/passwd contents must NOT appear.
    assert!(
        !content.contains("root:") && !content.contains("/bin/bash") && !content.contains(":0:0:"),
        "SECURITY: XXE external-entity content leaked (looks like /etc/passwd): {content:?}"
    );
    // The expanded entity token (if expansion happened) would multiply 'lol'.
    // A non-expanding scanner keeps the literal `&lol9;` reference at most once.
    assert!(
        content.matches("lol").count() < 100,
        "SECURITY: 'lol' appears {} times — entity expansion occurred: {content:?}",
        content.matches("lol").count()
    );
}

// ── 4. OVERSIZED SINGLE ENTRY (genuinely large, not a ratio trick) ──────────

/// docx whose word/document.xml is a single genuinely-large (~50 MB) entry. This
/// probes an output-size bound distinct from the high-ratio bomb. 50 MB is under
/// the 64 MB cap so it is ACCEPTED today (bounded, no hang) — this pins that a
/// legitimately-large-but-under-cap doc still parses, and that the cap is what
/// separates it from the >64 MB bomb (raise the bomb above the cap → rejected).
#[test]
fn docx_big_single_entry_bounded_accepted() {
    let res = parse_with_timeout(fixture("big_entry.docx"), "big_entry.docx", DEADLINE);
    let (_title, content) = res.expect(
        "a ~50 MB single-entry docx (under the 64 MB cap) must parse bounded, not error/hang",
    );
    // It parsed; content is large but bounded by the (under-cap) entry size.
    assert!(!content.is_empty(), "big entry must yield content");
    assert!(
        content.len() <= 64 * 1024 * 1024,
        "content {} bytes must be bounded by the per-entry cap",
        content.len()
    );
}

// ── 5. GARBAGE / TRUNCATED CONTAINERS → graceful Err, never panic/hang ──────

/// Truncated or non-zip bytes with office extensions must produce a graceful
/// Err (or empty), never a panic or hang. This is the cheap-but-essential
/// "malformed upload" robustness leg.
#[test]
fn malformed_office_containers_graceful_error() {
    for name in ["junk.docx", "junk.xlsx", "junk.pptx", "junk.epub"] {
        let data = b"PK\x03\x04 truncated not-a-real-zip \x00\xff".to_vec();
        let res = parse_with_timeout(data, leak_name(name), Duration::from_secs(10));
        // Either Err (most formats) — must NOT panic/hang (the watchdog enforces that).
        // calamine/zip may also return an empty Ok for some truncations; both are
        // "bounded + safe". The hard requirement is: it returned, no panic.
        let _ = res;
    }
}

/// `parse_bytes` takes a `&'static str` filename in our timeout helper; produce a
/// leaked static for the test-only filenames above.
fn leak_name(s: &str) -> &'static str {
    Box::leak(s.to_string().into_boxed_str())
}
