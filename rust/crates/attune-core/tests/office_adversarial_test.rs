//! Office / container ADVERSARIAL tests — the P0 security dimension.
//!
//! Dimension owner: §2.6 matrix axis C (鲁棒 6 类) → the OFFICE ZIP/XML
//! adversarial gap (解压炸弹 / entry 路径穿越 / billion-laughs XXE / 超大单条目).
//! docx/xlsx/pptx/epub are ZIP+XML containers parsed from UNTRUSTED uploads, so a
//! crafted file must never hang, OOM, or escape the filesystem.
//!
//! "证明它会挂" mindset: each case crafts a malicious input and asserts the parser
//! is BOUNDED — it returns within a wall-clock deadline (a hang FAILS the test via
//! a watchdog thread + recv_timeout) and either errors or safe-truncates, never
//! blowing up memory or writing outside the intended area.
//!
//! Fixtures: committed under `tests/fixtures/office/`, (re)generated by
//! `scripts/gen-office-fixtures.py`. The bomb fixtures are a few-KB-to-100KB on
//! disk but decompress to ~100 MB — that ratio is the whole point.
//!
//! SECURITY GUARD UNDER TEST: `parser.rs::read_zip_entry_string_bounded` +
//! `MAX_ZIP_ENTRY_BYTES` (64 MB per-entry / cumulative decompression cap) added
//! alongside this suite. The bomb cases below are the regression locks for that
//! guard (they REJECT the >64 MB bombs). Path-traversal + XXE/billion-laughs are
//! inert-by-construction (memory-only extraction + a non-entity-expanding XML
//! scanner) and the tests pin that.

use std::path::PathBuf;
use std::sync::mpsc;
use std::time::{Duration, Instant};

use attune_core::parser::parse_bytes;

fn office_dir() -> PathBuf {
    PathBuf::from(env!("CARGO_MANIFEST_DIR")).join("tests/fixtures/office")
}

fn fixture(name: &str) -> Vec<u8> {
    let p = office_dir().join(name);
    std::fs::read(&p).unwrap_or_else(|e| {
        panic!(
            "missing office fixture {} at {} ({e}); run `python3 scripts/gen-office-fixtures.py`",
            name,
            p.display()
        )
    })
}

/// Run `parse_bytes(data, filename)` on a watchdog thread; FAIL if it does not
/// return within `deadline` (a hang / unbounded loop must not pass as "bounded").
/// Returns the parser's `Result` (Ok or Err — both are "bounded", the test
/// decides which it expects).
fn parse_with_timeout(
    data: Vec<u8>,
    filename: &'static str,
    deadline: Duration,
) -> Result<(String, String), String> {
    let (tx, rx) = mpsc::channel();
    let t0 = Instant::now();
    let handle = std::thread::Builder::new()
        .name(format!("parse-{filename}"))
        // Generous stack: a recursive/entity-expanding parser would blow this;
        // attune's scanners are iterative so this is comfortable headroom.
        .stack_size(8 * 1024 * 1024)
        .spawn(move || {
            let r = parse_bytes(&data, filename).map_err(|e| e.to_string());
            let _ = tx.send(r);
        })
        .expect("spawn watchdog thread");

    match rx.recv_timeout(deadline) {
        Ok(result) => {
            let _ = handle.join();
            let elapsed = t0.elapsed();
            assert!(
                elapsed < deadline,
                "{filename} parse returned but took {elapsed:?} (>= deadline {deadline:?})"
            );
            result
        }
        Err(mpsc::RecvTimeoutError::Timeout) => {
            // The worker is still running — a HANG. We cannot kill it, but the
            // test must fail. (The process will exit at test-binary teardown.)
            panic!(
                "SECURITY: {filename} parse HUNG past {deadline:?} — not bounded (possible \
                 decompression bomb / infinite expansion / pathological loop)"
            );
        }
        Err(mpsc::RecvTimeoutError::Disconnected) => {
            panic!("{filename} watchdog thread died without sending a result")
        }
    }
}

// A wall-clock that is comfortably above a legitimate parse of a tiny file but
// far below "OOM-then-swap-death". A bounded reject of a 100 MB-decompressed
// entry completes in well under a second on dev hardware.
const DEADLINE: Duration = Duration::from_secs(20);

// ── 1. ZIP-BOMB (high decompression ratio) ──────────────────────────────────

/// docx zip-bomb: word/document.xml is a few-KB deflate that inflates to
/// ~100 MB. With the `MAX_ZIP_ENTRY_BYTES` guard the parser must REJECT it
/// (Err) rather than allocate ~100 MB — and must return within the deadline.
#[test]
fn docx_zip_bomb_rejected_bounded() {
    let res = parse_with_timeout(fixture("bomb.docx"), "bomb.docx", DEADLINE);
    let err = res.expect_err(
        "docx zip-bomb must be REJECTED by the decompression-size guard, not parsed \
         (a ~100 MB inflate is the DoS). If this is Ok, the MAX_ZIP_ENTRY_BYTES guard \
         in parser.rs regressed.",
    );
    assert!(
        err.contains("zip bomb") || err.contains("exceeds") || err.contains("decompressed"),
        "rejection error should name the decompression bound: {err}"
    );
}

/// xlsx zip-bomb: sheet1.xml inline-string inflates to ~100 MB. The pre-scan of
/// declared uncompressed entry sizes (before calamine materializes the sheet)
/// must REJECT it bounded.
#[test]
fn xlsx_zip_bomb_rejected_bounded() {
    let res = parse_with_timeout(fixture("bomb.xlsx"), "bomb.xlsx", DEADLINE);
    let err = res.expect_err(
        "xlsx zip-bomb must be REJECTED by the pre-calamine entry-size scan, not handed \
         to calamine which would materialize ~100 MB. If Ok, the xlsx bomb guard regressed.",
    );
    assert!(
        err.contains("zip bomb") || err.contains("exceeds") || err.contains("decompressed"),
        "rejection error should name the decompression bound: {err}"
    );
}

/// FLAG (BUG-3b, WARNING/security, #[ignore] follow-up): the xlsx bomb guard
/// (`parser.rs::xlsx_bytes_to_text` pre-scan) trusts `ZipFile::size()`, which
/// `zip` reads verbatim from the central-directory `uncompressed_size`
/// (zip-2.4.2 read.rs:1733) — an ATTACKER-CONTROLLABLE field. A crafted xlsx can
/// declare a small `size()` (passing the scan) while its deflate stream inflates
/// to GB; calamine then accumulates the real bytes (it has no internal byte cap),
/// re-opening the OOM hole for xlsx specifically. The honest bomb (truthful
