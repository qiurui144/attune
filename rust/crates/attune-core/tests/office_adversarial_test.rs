/// `take(MAX_ZIP_ENTRY_BYTES)` idiom from `read_zip_entry_string_bounded`) before
/// handing the workbook to calamine, or switch to an xlsx reader that caps
/// in-memory string accumulation. This test is #[ignore] until that lands; it
/// crafts a spoofed-size bomb and documents that the current declared-size scan
/// does NOT reject it (so when the real fix lands, flip the assertion to
